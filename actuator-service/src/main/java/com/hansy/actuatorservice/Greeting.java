package com.hansy.actuatorservice;

/**
 * @Author: hansy
 * @Date: 2020/11/24
 */
public class Greeting {

    private final long id;
    private final String content;

    public Greeting(long id, String content) {
        this.id = id;
        this.content = content;
    }

    public long getId() {
        return id;
    }

    public String getContent() {
        return content;
    }
}
